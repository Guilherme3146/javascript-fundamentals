// startWith

const text = 'Hola mundo';

const start = text.startsWith('Hola');

console.log(start); // true

const startPosition = text.startsWith('mundo', 5);

console.log(startPosition); // true, começa a buscar a partir do índice 5

// endWith

const end = text.endsWith('mundo');

console.log(end); // true   

